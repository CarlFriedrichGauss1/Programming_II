import math

def polygon(n):
    sum = 0
    for i in range(len(n)-1):
        sum += abs((n[i][0]*n[i+1][1] - n[i+1][0]*n[i][1]) + (n[-1][0]*n[0][1] - n[0][0]*n[-1][1]))

    return sum/2

